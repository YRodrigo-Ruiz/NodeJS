let traductor = "PUERTA";

switch (traductor.toLowerCase()) {
    case "puerta":
        console.log("Door"); 
        break;
    case "gato":
        console.log("Cat");
        break;
    case "ventana":
        console.log("Window");
        break;    
    case "mesa":
        console.log("Table");
        break;
    default: console.log("La palabra ingresada no es correcta");
        break;
}

